# ------------------------------------------------------------ class: Country ------------------------------------------------------------ #

class Country(BaseObject):

    # ------------------------------------------------------------- Init ------------------------------------------------------------- #

    def __init__(
        self,
        d: dict
    ):
        super().__init__(d)

        self.total_games = self._d_val(d, 'totalGames', 0)
        self.live_games = self._d_val(d, 'liveGames', 0)

        self.sport_types = [SportType(v) for v in d['sportTypes']]


